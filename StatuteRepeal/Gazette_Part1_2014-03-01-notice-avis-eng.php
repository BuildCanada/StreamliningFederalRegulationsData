    <h3 class="text-center">INTERIM ORDER RESPECTING FLIGHTS TO RUSSIA</h3>
    <h4 class="text-center">INTERPRETATION</h4>
    <p>Terminology: <cite>Canadian Aviation Security Regulations, 2012</cite></p>
    <p class="indent-1-0"><strong>1.</strong> Unless the context requires otherwise, words and expressions used in this Interim Order have the same meaning as in section&nbsp;3 of the <cite>Canadian Aviation Security Regulations, 2012</cite>.</p>
    <h4 class="text-center">APPLICATION AND CONFLICT</h4>
    <p>Application and conflict</p>
    <p class="indent-1-0"><strong>2.</strong> This Interim Order applies in addition to the <cite>Canadian Aviation Security Regulations, 2012</cite> and their related security measures. If there is a conflict between this Interim Order and the <cite>Canadian Aviation Security Regulations, 2012</cite> or their related security measures, this Interim Order prevails to the extent of the conflict.</p>
    <h4 class="text-center">SCREENING AUTHORITY</h4>
    <p>Screening</p>
    <p class="indent-1-0"><strong>3.</strong> (1) The screening authority at an aerodrome listed in the schedule to the <cite>CATSA Aerodrome Designation Regulations</cite> must not allow a passenger for a flight to Russia to pass beyond a passenger screening checkpoint into a sterile area unless the screening authority screens the passenger and any goods in his or her possession or control for liquids, aerosols, gels for powders.</p>
    <p>Prohibited goods</p>
    <p class="indent-1-0">(2) The screening authority must not allow a passenger for a flight to Russia who is in possession or control of a liquid, aerosol, gel or powder to pass beyond the passenger screening checkpoint into a sterile area.</p>
    <p>Exceptions</p>
    <p class="indent-1-0">(3) Subsection&nbsp;(2) does not apply in respect of</p>
    <ul class="oo-en">
        <li>(<cite>a</cite>) a liquid, aerosol, gel or powder that is formula, milk, juice or other food for a child who is under two years of age;</li>
        <li>(<cite>b</cite>) a liquid, aerosol, gel or powder that is a medically-necessary good; or</li>
        <li>(<cite>c</cite>) a duty-free liquid, aerosol, gel that is in a security tamper-evident bag that is sealed and shows no signs of tampering.</li>
    </ul>
    <h4 class="text-center"> AIR CARRIER</h4>
    <p>Subsection 4.85(3) of the Act</p>
    <p class="indent-1-0"><strong>4.</strong> For greater certainty, an air carrier that operates a flight to Russia from an aerodrome listed in the schedule to the <cite>CATSA Aerodrome Designation Regulations</cite> is prohibited under subsection&nbsp;4.85(3) of the Act from transporting on the flight a passenger or any goods in his or her possession or control unless the passenger and goods have been screened in accordance with this Interim Order.</p>
    <h4 class="text-center">CESSATION OF EFFECT</h4>
    <p>Cessation of effect</p>
    <p class="indent-1-0"><strong>5.</strong> This Interim Order ceases to have effect on February&nbsp;24, 2014 if it is approved under subsection&nbsp;6.41(2) of the Act.</p>
