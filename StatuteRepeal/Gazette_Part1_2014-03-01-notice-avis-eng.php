    <p>Salary range: $255,200&ndash;$300,200 <br>
        Location: Ottawa, Ontario</p>
    <p class="indent-1-0">The Royal Canadian Mint (the Mint) was originally established in 1908 as a branch of the Royal Mint of London, England. On December 1, 1931, an Act of Parliament established the Royal Canadian Mint as a branch of the Department of Finance. On April 1, 1969, with the passage of the <cite>Royal Canadian Mint Act</cite>, the Mint became a Crown corporation and now reports to Parliament through the Minister of Finance.</p>
    <p class="indent-1-0">Today, the Mint is a commercial enterprise whose business ventures extend throughout the world. The Mint is classified as a Schedule III-II Corporation under the <cite>Financial Administration Act</cite>, the category reserved for organizations that conduct commercial operations and are self-sufficient. The Mint owns two facilities. The Ottawa facility houses the corporate head office, the gold and silver refineries and production facilities for non-circulation coins and medals. The Winnipeg plant, which includes a plating facility, produces all of Canada's circulation coins as well as circulation coins for numerous foreign countries. The Mint is an ISO 9001-2008 certified corporation. The Mint operates along four business lines:</p>
    <ul>
        <li>Canadian Circulation: The design, manufacture and distribution of circulation coins to meet the needs of the Canadian marketplace.</li>
        <li>Numismatics and Collectibles: The design, manufacture and distribution of Canadian non-circulation coin products for sale to collectors and gift-givers.</li>
        <li>Foreign: The manufacture of circulation and numismatic coins and blanks for foreign countries.</li>
        <li>Bullion, Refinery and ETR (Exchange Traded Receipts): The production of bullion coins, wafers and bars for the investment community; refining and storage services of precious metals for Canadian and international customers; and access for retail and institutional investors to precious metals stored at the Mint through gold and silver Exchange Traded Receipts (ETR) products listed on the Toronto Stock Exchange (TSX).</li>
    </ul>
    <p class="indent-1-0">As the Chief Executive Officer of the Corporation, the Master of the Mint reports to the Board of Directors and is responsible for providing leadership and direction in all aspects of the Corporation's activities, business performance and strategic development. The Board of Directors is accountable to Parliament through the Minister of Finance.</p>
    <p class="indent-1-0">The ideal candidate would possess a degree from a recognized university in a relevant field of study, or an acceptable combination of education, job-related training and/or experience.</p>
    <p class="indent-1-0">The ideal candidate would have a proven record, with significant experience as a chief executive officer or senior executive in a large, complex organization within a dynamic environment. The candidate would possess experience leading and developing high performance teams and managing financial resources, ideally with exposure to a unionized workforce. Experience providing strategic advice and making decisions on complex and sensitive issues, as well as experience in building and maintaining productive and multipartite relationships with partner organizations and diverse stakeholders, is desired. He or she would also have experience in reporting to, and/or working with, a board of directors of a large complex organization of significant size and scope. Experience working in a public policy environment or government organization and/or experience in a commercial environment with a record of generating revenue and profits within a customer-focused organization, preferably including at the international level, are sought.</p>
    <p class="indent-1-0">The ideal candidate would be knowledgeable about the mandate and business activities of the Mint, its related public policy issues, as well as its governing legislation framework, including the <cite>Royal Canadian Mint Act</cite> and the <cite>Financial Administration Act</cite>. Knowledge and understanding of the Mint's business lines (Canadian Circulation; Numismatics and Collectibles; Foreign; and Bullion, Refinery and Exchange Traded Receipts), including its operations, risk management and competitive dynamics on an international scale, are desirable. The candidate would have an understanding of sound business practices and principles, including human resources and financial management, as well as good corporate governance. General knowledge of the business and political environments, along with stakeholders served by the Mint, is sought. He or she would also be familiar with Canadian government, policies, practices and decision-making frameworks, and possess an appreciation of quality measures within a lean manufacturing setting.</p>
    <p class="indent-1-0">The ideal candidate would have the ability to appreciate the needs of diverse stakeholders, develop and articulate a corporate vision, and convert this into a strategic action plan. The ability to lead with strategic agility while inspiring a high performance workforce and building an entrepreneurial culture of teamwork and customer-focus is sought, as is the ability to establish and maintain strong and effective relationships with businesses, government and external stakeholders domestically and abroad. The candidate would also have the ability to identify and pursue innovative initiatives for increasing revenue generation through the development and implementation of new business opportunities/partnerships. He or she would be able to negotiate skilfully, as well as influence the opinions and ideas of others, build consensus and gain approval for plans, programs, and recommended courses of action. Superior communication skills, both written and oral, are also sought.</p>
    <p class="indent-1-0">To achieve the Corporation's objectives and carry out its mandate, the Master of the Mint would be an innovative, entrepreneurial and strategic leader, who possesses sound business judgement and is a person of high ethical standards and integrity. He or she would possess superior interpersonal skills as an inspirational leader and motivator of people, and use tact and discretion in relating with others.</p>
    <p class="indent-1-0">Proficiency in both official languages would be preferred.</p>
    <p class="indent-1-0">The Mint's head office is located in Ottawa; therefore, the successful candidate must reside in, or be willing to relocate to, the Ottawa area or to a location that is within reasonable commuting distance.</p>
    <p class="indent-1-0">A person is not eligible to be appointed if the person is not a Canadian citizen ordinarily resident in Canada or if, directly or indirectly, the person is engaged in any undertaking involving or associated with</p>
    <ul class="ii">
        <li>(i) the production or distribution of copper, copper alloy, nickel or precious metals;</li>
        <li>(ii) the purchase, production, distribution or sale of coins or coinoperated devices; or</li>
        <li>(iii) the vending of goods and services by means of coinoperated devices.</li>
    </ul>
    <p class="indent-1-0">The Government is committed to ensuring that its appointments are representative of Canada's regions and official languages, as well as of women, Aboriginal peoples, disabled persons and visible minorities.</p>
    <p class="indent-1-0">The selected candidate must comply with the <cite>Ethical and Political Activity Guidelines for Public Office Holders</cite>. The Guidelines are available on the Governor in Council Appointments Web site, under &ldquo;Reference Material,&rdquo; at <a href="http://www.appointments-nominations.gc.ca/index.asp?lang=eng">www.appointments-nominations.gc.ca/index.asp?lang=eng</a>.</p>
    <p class="indent-1-0">The selected candidate will be subject to the <cite>Conflict of Interest Act</cite>. Public office holders appointed on a full-time basis must submit to the Office of the Conflict of Interest and Ethics Commissioner, within 60&nbsp;days of appointment, a confidential report in which they disclose all of their assets, liabilities and outside activities. For more information, please visit the Office of the Conflict of Interest and Ethics Commissioner's Web site at <a href="http://ciec-ccie.gc.ca/Default.aspx?pid=1&amp;lang=en">http://ciec-ccie.gc.ca/Default.aspx?pid=1&amp;lang=en</a>.</p>
    <p class="indent-1-0">This notice has been placed in the <cite>Canada&nbsp;Gazette</cite> to assist the Governor in Council in identifying qualified candidates for this position. It is not, however, intended to be the sole means of recruitment.</p>
    <p class="indent-1-0">Further details about the Mint and its activities can be found on its Web site at <a href="http://www.mint.ca">www.mint.ca</a>.</p>
    <p class="indent-1-0">For more information, please contact Michelle Richard at 613-742-3212 or Richard Rankin at 613-742-3207, or either of them by email at rrankin@boyden.com. To apply for this position, please visit the Boyden Web site at <a href="http://www.boyden.ca">www.boyden.ca</a>.</p>
