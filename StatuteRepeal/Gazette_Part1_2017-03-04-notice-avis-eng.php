    <h1 id="wb-cont">Canada Gazette, Part I, Volume 151, Number 9:&nbsp;titre</h1>
    <p>March 4, 2017</p>
    <h2 class="publication">GOVERNMENT NOTICES <br>
    DEPARTMENT OF CITIZENSHIP AND IMMIGRATION</h2>
    <h3>IMMIGRATION AND REFUGEE PROTECTION ACT</h3>
    <h4><cite>Ministerial Instructions for the Processing of Applications under the Atlantic Immigration Pilot</cite></h4>
    <p >The Minister of Citizenship and Immigration, pursuant to section&nbsp;87.3 of the <cite>Immigration and Refugee Protection Act</cite>, makes the annexed <cite>Ministerial Instructions for the Processing of Applications under the Atlantic Immigration Pilot.</cite></p>
    <p >Ottawa, March 1, 2017</p>
    <p><strong>Ahmed D. Hussen</strong><br>
        Minister of Citizenship and Immigration</p>
    <h3>Ministerial Instructions for the Processing of Applications under the Atlantic Immigration Pilot</h3>
    <p >These Instructions are published in the <cite>Canada Gazette</cite> in accordance with subsection&nbsp;87.3(6) of the <cite>Immigration and Refugee Protection Act.</cite> The Minister of Citizenship and Immigration has established the following Ministerial Instructions that, in the opinion of the Minister, will best support the attainment of the immigration goals established by the Government of Canada.</p>
    <p><strong>Overview</strong></p>
    <p >These Instructions are given pursuant to section&nbsp;87.3 of the <cite>Immigration and Refugee Protection Act</cite> (IRPA). The Instructions are directed to officers and Minister's delegates who are charged with handling and/or reviewing applications for permanent residence.</p>
    <p >The Ministerial Instructions identify conditions in order for applications made under the Atlantic Canada Intermediate Skilled Worker Class, the Atlantic Canada Highly Skilled Worker Class and the Atlantic Canada International Graduate Class to be processed. These instructions support the Atlantic Immigration Pilot that is a part of the Atlantic Growth Strategy, which aims to help address labour market needs in the four Atlantic provinces by developing approaches for immigration to enhance retention of immigrant workers in the region and in support of broader Government of Canada objectives to foster economic growth in Atlantic Canada.</p>
    <p >The Instructions are consistent with IRPA objectives as laid out in section&nbsp;3, specifically to pursue the maximum social, cultural and economic benefits of immigration; to enrich and strengthen the social and cultural fabric of Canadian society; to support the development of a strong and prosperous Canadian economy; and to protect public health and safety and to maintain the security of Canadian society.</p>
    <p >The Instructions are compliant with the <cite>Canadian Charter of Rights and Freedoms</cite>.</p>
    <p >The Instructions respect all previously established accords and agreements, and all existing agreements with provinces and territories.</p>
    <p><strong>Scope </strong> </p>
    <p >These Instructions apply to applications received by designated Citizenship and Immigration Canada&nbsp;(CIC) offices on or after March&nbsp;6, 2017, until December&nbsp;31, 2017, under the Atlantic Canada Intermediate Skilled Worker Class, the Atlantic Canada Highly Skilled Worker Class and the Atlantic Canada International Graduate Class<cite>.<br>
        </cite></p>
    <h3>Intake of permanent resident applications under the&nbsp;Atlantic Canada Intermediate Skilled Worker Class, the&nbsp;Atlantic Canada Highly Skilled Worker Class and the Atlantic Canada International Graduate Class &mdash; Conditions for processing </h3>
    <h4>Cap and sub-caps on the number of applications to be accepted for processing in a year </h4>
    <p > A maximum of 2 000 applications submitted under the combined Atlantic Canada Intermediate Skilled Worker Class, the Atlantic Canada Highly Skilled Worker Class and the Atlantic Canada International Graduate Class will be accepted for processing for the 2017&nbsp;calendar year (from March&nbsp;6, 2017, to December&nbsp;31, 2017), unless otherwise indicated in a future Ministerial Instruction. Within the 2 000 cap, a maximum of 646&nbsp;applications submitted with a letter of endorsement from New Brunswick will be accepted for processing; 792&nbsp;applications submitted with a letter of endorsement from Nova Scotia will be accepted for processing; 442&nbsp;applications submitted with a letter of endorsement from Newfoundland and Labrador will be accepted for processing and 120&nbsp;applications submitted with a letter of endorsement from Prince Edward Island will be accepted for processing.</p>
    <h4>Instructions for processing Atlantic Intermediate Canada Skilled Worker Class, Atlantic Canada Highly Skilled Worker Class and Atlantic Canada International Graduate Class applications<br>
        </h4>
    <p >For an application to be considered a complete application under the Atlantic Canada Intermediate Skilled Worker Class, the Atlantic Canada Highly Skilled Worker Class and the Atlantic Canada International Graduate Class, it must meet the application kit requirements in place at the time the application is received by the Centralized Intake Office in Sydney, Nova Scotia.</p>
    <p >CIC will accept permanent resident applications received under the Atlantic Canada Intermediate Skilled Worker Class, the Atlantic Canada Highly Skilled Worker Class and the Atlantic Canada International Graduate Class starting on March&nbsp;6, 2017. For the period of March&nbsp;6, 2017 to December&nbsp;31, 2017, the first 2&nbsp;000&nbsp;complete applications received will be put into processing, subject to caps by province.</p>
    <p >In all cases, applicants meeting the conditions set out in&nbsp;the Ministerial Instructions are still subject to the Atlantic Canada Intermediate Skilled Worker Class, the Atlantic Canada Highly Skilled Worker Class and the Atlantic Canada International Graduate Class requirements and all other applicable requirements of the IRPA.</p>
    <p><strong>Humanitarian and compassionate requests </strong> </p>
    <p >A request made under subsection&nbsp;25(1) of the Act from outside Canada that accompanies an application that was not accepted for processing under these Instructions will not be processed.</p>
    <p><strong>Disposition of applications </strong> </p>
    <p >Any application that does not meet the conditions established by these Instructions will be returned. </p>
    <p><strong>Coming into effect </strong> </p>
    <p >The Instructions come into force on March&nbsp;6, 2017.</p>
    <p class="noticeInfo">[9-1-o]</p>
    <h2  id="ne2">DEPARTMENT OF CITIZENSHIP AND IMMIGRATION</h2>
    <h3>IMMIGRATION AND REFUGEE PROTECTION ACT</h3>
    <h4><cite>Ministerial Instructions Respecting the Atlantic Canada Intermediate Skilled Worker&nbsp;Class</cite></h4>
    <p >The Minister of Citizenship and Immigration, pursuant to section&nbsp;14.1 <a href="#footnote.60340" id="footnoteRef.60340">(see footnote a)</a> of the Immigration and Refugee Protection Act <a href="#footnote.60339" id="footnoteRef.60339">(see footnote b)</a>, makes the annexed Ministerial Instructions Respecting the Atlantic Canada Intermediate Skilled Worker Class.</p>
    <p >Ottawa, March 1, 2017</p>
    <p ><strong>Ahmed D. Hussen</strong><br>
        Minister of Citizenship and Immigration</p>
    <h3>Ministerial Instructions Respecting the Atlantic Canada Intermediate Skilled Worker&nbsp;Class</h3>
    <p ><strong>Definitions </strong></p>
    <p ><strong>1 </strong>The following definitions apply in these Instructions.</p>
    <p ><strong><cite>Act</cite></strong> means the <cite>Immigration and Refugee Protection Act</cite>. (<span xml:lang="fr" lang="fr">Loi</span>)</p>
    <p ><strong><cite>Atlantic province</cite></strong> means either Nova Scotia, New Brunswick, Prince Edward Island or Newfoundland and Labrador. (<span xml:lang="fr" lang="fr">province de l'Atlantique</span>)</p>
    <p ><strong><cite>Canadian educational credential</cite></strong> has the same meaning&nbsp;as in subsection&nbsp;73(1) of the Regulations. (<span xml:lang="fr" lang="fr">dipl&ocirc;me canadien</span>)</p>
    <p ><strong><cite>full-time work</cite></strong> has the same meaning as in subsection&nbsp;73(1) of the Regulations. (<span xml:lang="fr" lang="fr">travail &agrave; temps plein</span>)</p>
    <p ><strong><cite>language skill area</cite></strong> has the same meaning as in subsection&nbsp;73(1) of the Regulations. (<span xml:lang="fr" lang="fr">habilet&eacute; langagi&egrave;re</span>)</p>
    <p ><strong><cite>Regulations</cite></strong> means the <cite>Immigration and Refugee Protection Regulations</cite>. (<span xml:lang="fr" lang="fr">R&egrave;glement</span>)</p>
    <p ><strong><cite>restricted occupation</cite></strong> has the same meaning as in subsection&nbsp;73(1) of the Regulations. (<span xml:lang="fr" lang="fr">profession d'acc&egrave;s limit&eacute;</span>)</p>
    <p ><strong><cite>work</cite></strong> has the same meaning as in subsection&nbsp;73(2) of the Regulations. (<span xml:lang="fr" lang="fr">travail</span>)</p>
    <p ><strong>Atlantic Canada intermediate skilled worker class<br>
        </strong></p>
    <p ><strong>2 (1) </strong>The Atlantic Canada Intermediate Skilled Worker Class is established as part of the economic class referred to in subsection&nbsp;12(2) of the Act and consists of foreign nationals who intend to reside in an Atlantic province, who have the ability to become economically established in that province and who meet the requirements of these Instructions.</p>
    <p ><strong>Selection requirements </strong></p>
    <p ><strong>(2) </strong>A foreign national is a member of the class if, at the time of their application for permanent residence,</p>
    <ul class="lst-spcd mrgn-lft-lg  list-unstyled">
        <li><strong>(a) </strong>they show, using results of a language test&nbsp;&mdash; approved under subsection&nbsp;74(3) of the Regulations and provided by an institution or organization designated under that subsection&nbsp;&mdash; that are less than two years old, that their proficiency in an official language is assessed at level 4 or higher in each of the four language skill areas under either the <cite>Canadian Language Benchmarks</cite> or the <span xml:lang="fr" lang="fr"><cite>Niveaux de comp&eacute;tence linguistique canadiens</cite>;</span></li>
        <li><strong>(b) </strong>they hold either a Canadian educational credential or both a foreign diploma, certificate or credential and&nbsp;an equivalency assessment described in subsection&nbsp;73(1) of the Regulations, which assessment must be less than five years old;</li>
        <li><strong>(c) </strong>they have, in the preceding three years, accumulated at least one year of full-time work experience, or the equivalent in part-time work, in an occupation listed in Skill Level&nbsp;C of the <cite>National Occupational Classification</cite> matrix that is not a restricted occupation and, while working in that occupation, they
            <ul class="lst-spcd mrgn-lft-lg  list-unstyled">
                <li><strong>(i) </strong>performed the actions described in the lead statement for the occupation as set out in the occupational descriptions of the <cite>National Occupational Classification</cite>, and</li>
                <li><strong>(ii) </strong>performed a substantial number of the main duties of the occupation as set out in the occupational descriptions of the <cite>National Occupational Classification</cite>, including all of the essential duties;</li>
            </ul>
        </li>
        <li><strong>(d) </strong>they have received from a recognized employer&nbsp;&mdash; that is, an employer that is designated by an Atlantic province in accordance with an immigration agreement to make employment offers and is not a consulate or an&nbsp;employer who is referred to in any of subparagraphs&nbsp;200(3)(h)(i) to (iii) of the Regulations&nbsp;&mdash; an offer of employment that they are able to perform and likely to accept and carry out and they meet the relevant employment requirements of the occupation as set out in the <cite>National Occupational Classification</cite> except for certification requirements in the case of regulated occupations and the offered employment is
            <ul class="lst-spcd mrgn-lft-lg  list-unstyled">
                <li><strong>(i) </strong>full&ndash;time, non-seasonal work for an indeterminate period, and</li>
                <li><strong>(ii) </strong>in an occupation that is listed in Skill Type&nbsp;0 Management Occupations or Skill Level&nbsp;A, B or C of the <cite>National Occupational Classification</cite> matrix;</li>
            </ul>
        </li>
        <li><strong>(e) </strong>they have been endorsed by an Atlantic province in accordance with an immigration agreement and they intend to reside in that province; and</li>
        <li><strong>(f) </strong>in the case of a foreign national who is not already working in Canada, they have in the form of transferable and available funds, unencumbered by debts or other obligations, an amount equal to one eighth of the amount identified, in the most recent edition of the publication concerning low income cut-offs that is published by Statistics Canada under the <cite>Statistics Act</cite> for urban areas of residence of 500,000&nbsp;persons or more, as the minimum amount of before-tax annual income necessary to support the foreign national and their family members, whether they are accompanying the foreign national or not.<br>
        </li>
    </ul>
    <p ><strong>Language proficiency </strong></p>
    <p ><strong>(3) </strong>To demonstrate that they meet the requirements of paragraph&nbsp;(2)(a), the foreign national must provide an original document, issued by an organization or institution referred to in that paragraph, setting out the results that they obtained in the language test.</p>
    <p ><strong>Eligible work experience </strong></p>
    <p ><strong>(4) </strong>For the purpose of paragraph&nbsp;(2)(c), work experience is only considered if it was obtained by the foreign national in the employ of a third party and, if it was obtained in Canada, the foreign national was both authorized to work in Canada and was a temporary resident.</p>
    <p ><strong>Application from within Canada </strong></p>
    <p ><strong>3 </strong>If a foreign national is in Canada at the time of their application for permanent residence, they must have temporary resident status.</p>
    <p ><strong>Permanent resident visa </strong></p>
    <p ><strong>4 (1) </strong>For greater certainty, subsections&nbsp;70(1), (2), (4) and&nbsp;(5) of the Regulations apply to permanent resident visa applications made by foreign nationals in the Atlantic Canada intermediate skilled worker class.</p>
    <ul class="noBulletIndex">
        <li><br>
            <strong>Visa issuance </strong></li>
        <li><strong>(2) </strong>In order for the visa to be issued</li>
    </ul>
    <ul class="lst-spcd mrgn-lft-lg  list-unstyled">
        <li><strong>(a) </strong>the offer of employment described in paragraph&nbsp;2(2)(d) must still be valid or the foreign national must be occupying an employment that meets the requirements of that paragraph; and</li>
        <li><strong>(b) </strong>the endorsement referred to in paragraph&nbsp;2(2)(e) must have been issued within the preceding six months and must not have been revoked by the issuing province.</li>
    </ul>
    <p ><strong>Family member </strong></p>
    <p ><strong>5 </strong>A foreign national who is an accompanying family member of a person who makes an application as a member of the Atlantic Canada intermediate skilled worker class becomes a permanent resident if, following an examination, it is established that</p>
    <ul class="lst-spcd mrgn-lft-lg  list-unstyled">
        <li><strong>(a) </strong>the person who made the application has become a permanent resident; and</li>
        <li><strong>(b) </strong>the foreign national is not inadmissible.</li>
    </ul>
    <p ><strong>Effective period </strong></p>
    <p ><strong>6 </strong>These Instructions have effect during the period beginning on March&nbsp;6, 2017 and ending on March&nbsp;5, 2022.</p>
