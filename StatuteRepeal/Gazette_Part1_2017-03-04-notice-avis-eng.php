    <h3>EXPLANATORY NOTE </h3>
    <p>(<cite>This explanatory note is not part of the&nbsp;Significant New Activity Notice.</cite>)</p>
    <h4 class="headline">Description</h4>
    <p >A Significant New Activity Notice is a legal instrument adopted by the Minister of the Environment pursuant to section&nbsp;85 of the <cite>Canadian Environmental Protection&nbsp;Act,&nbsp;1999</cite> (CEPA) to apply the Significant New Activity (SNAc) provisions of that Act to 2-propanol, 1-[bis(2-hydroxyethyl)amino]-, Chemical Abstracts Service Registry No.&nbsp;6712-98-7. The Notice is now in force. It is therefore mandatory to meet all the requirements of the Notice should a person intend to use the substance for a significant new activity as defined in the Notice. <a href="#footnote.53283" id="footnoteRef.53283">(see footnote 3)</a></p>
    <p >A Significant New Activity Notice does not constitute an endorsement from the Department of the Environment or the Government of Canada of the substance to which it relates, or an exemption from any other laws or regulations that are in force in Canada and that may apply to this substance or activities involving the substance.</p>
    <h4 class="headline">Applicability of the Significant New Activity Notice </h4>
    <p >The Notice requires that any person (individual or corporation) engaging in a significant new activity in relation to 2-propanol, 1-[bis(2-hydroxyethyl)amino]-, Chemical Abstracts Service Registry No.&nbsp;6712-98-7, submit a Significant New Activity Notification (SNAN) containing all of the information prescribed in the Notice at least 90&nbsp;days prior to using the substance for the significant new activity.</p>
    <p >In order to address human health concerns, the Notice targets the use of the substance in consumer products to which the <cite>Canada Consumer Product Safety Act</cite> (CCPSA) <a href="#footnote.53280" id="footnoteRef.53280">(see footnote 4)</a> applies, in natural health products as defined in subsection&nbsp;1(1) of the <cite>Natural Health Products Regulations</cite> <a href="#footnote.53277" id="footnoteRef.53277">(see footnote 5)</a> or any other drug as defined in the <cite>Food and Drugs Act</cite>, and in cosmetics as defined in section&nbsp;2 of the <cite>Food and Drugs Act</cite>. <a href="#footnote.53274" id="footnoteRef.53274">(see footnote 6)</a> A notification is required for the manufacture of such products with the substance.</p>
    <p >For any other activity related to any of these products, notification is required when, during a calendar year, the total quantity of the substance in the product is greater than 10&nbsp;kg. For example, notification is required if a person plans to use a consumer product (e.g. paint) where there is more than 10&nbsp;kg of the substance involved in a calendar year. Examples of products of concern include, but are not limited to, do-it-yourself products such as paints, coatings, adhesives, sealants, and epoxies. The substance is not known to be currently used in consumer products, in natural health products, in drugs or in cosmetics in Canada.</p>
    <h4 class="headline">Activities not subject to the Notice </h4>
    <p >Activities involving the use of the substance as a research and development substance, a site-limited intermediate or an export-only substance are excluded from the Notice. The terms &ldquo;research and development substance&rdquo; and &ldquo;site-limited intermediate substance&rdquo; are defined in subsection&nbsp;1(1) of the <cite>New Substances Notification Regulations (Chemicals and Polymers)</cite>. <a href="#footnote.53603" id="footnoteRef.53603">(see footnote 7)</a> An export-only substance is a substance that is manufactured in or imported into Canada and destined solely for foreign markets.</p>
