    <p >In Canada, substances in the Ethylene Glycol Ethers Group are used in a variety of applications, including cosmetics and non-prescription drugs, paint and coating products, and air fresheners, as well as in adhesives, batteries, and textiles.</p>
    <p >All of the substances in this group are imported into Canada in quantities ranging from 100&nbsp;to&nbsp;10 000&nbsp;000&nbsp;kg/year. Four&nbsp;of the seven&nbsp;substances&nbsp;(TTEG,&nbsp;DEGEE,&nbsp;EGBEA, and&nbsp;DEGBE) in this group are manufactured in Canada in quantities ranging from&nbsp;1&nbsp;000&nbsp;to&nbsp;10&nbsp;000&nbsp;000&nbsp;kg/year. In the United States, production volumes range from&nbsp;10&nbsp;000&nbsp;000 to&nbsp;450&nbsp;000&nbsp;000&nbsp;kg/year&nbsp;for these substances.</p>
    <p >The ecological risks of the seven substances in the Ethylene Glycol&nbsp;Ethers&nbsp;Group were characterized using the Ecological Risk Classification of organic substances (ERC). The ERC is a risk-based approach that employs multiple metrics for both hazard and exposure based on weighted consideration of multiple lines of evidence for determining risk classification. Hazard profiles are established based principally on metrics regarding mode of toxic action, chemical reactivity, food web&ndash;derived internal toxicity thresholds, bioavailability, and chemical and biological activity. Metrics considered in the exposure profiles include potential emission rate, overall persistence, and long-range transport potential. A risk matrix is used to assign a low, moderate or high level of potential concern for substances based on their hazard and exposure profiles. The&nbsp;ERC identified the seven substances in the Ethylene&nbsp;Glycol&nbsp;Ethers&nbsp;Group as having low potential to cause ecological harm.</p>
    <p >Considering all available lines of evidence presented in this draft screening assessment, there is a low risk of harm to organisms and the broader integrity of the environment from the seven substances in the Ethylene Glycol Ethers Group. It is proposed to conclude that the seven substances in the Ethylene Glycol Ethers Group do not meet the criteria under paragraph&nbsp;64(<cite>a</cite>)&nbsp;or&nbsp;(<cite>b</cite>) of CEPA, as they are not entering the environment in a quantity or concentration or under conditions that have or may have an immediate or long-term harmful effect on the environment or its biological diversity or that constitute or may constitute a danger to the environment on which life depends.</p>
    <p >For the human health risk assessment, the seven substances in this group were separated into&nbsp;three&nbsp;subgroups: ethylene glycols&nbsp;(EGs), ethylene glycol ethers&nbsp;(EGEs), and glymes. Environmental media and food were not identified as significant sources of exposure to Canadians. For the EGs and EGEs, estimates of exposure were derived based on levels of substances in products used by consumers, such as cosmetics and non-prescription drugs, paint and coating products, and household cleaning products. For monoglyme, estimates of exposure were based on levels in indoor air and from use of air fresheners.</p>
    <p >Systemic toxicity is observed at high dose levels in laboratory animals exposed to ethylene glycol ethers, with target organs being the liver and kidneys. For some of the substances (DEGEE, EGBEA, and DEGBE), hemolytic effects observed in laboratory animals at the doses tested are not of concern for humans, as humans are much less sensitive to these effects. For monoglyme, developmental toxicity is observed in laboratory animals at doses lower than those for the other ethylene glycol ether substances in this group, along with effects on testes, blood, thymus and adrenal glands. For all subgroups, based on the estimates of exposure for products used by consumers and critical effect levels identified from laboratory studies, margins of exposure are considered to be adequate to address uncertainties in the health effects and exposure databases.</p>
    <p >Based on the information presented in this draft screening assessment, it is proposed to conclude that the seven substances in the Ethylene&nbsp;Glycol&nbsp;Ethers&nbsp;Group do not meet the criteria under paragraph&nbsp;64(<cite>c</cite>) of CEPA, as they are not entering the environment in a quantity or concentration or under conditions that constitute or may constitute a danger in Canada to human life or health.</p>
    <h5>Proposed conclusion</h5>
    <p >It is proposed to conclude that the seven substances in the Ethylene&nbsp;Glycol&nbsp;Ethers&nbsp;Group do not meet any of the criteria set out in section&nbsp;64&nbsp;of&nbsp;CEPA. </p>
