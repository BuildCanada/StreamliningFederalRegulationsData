    <h2 id="ne11">DEPARTMENT OF NATURAL RESOURCES</h2>
    <h3>ENERGY EFFICIENCY ACT</h3>
    <h4><cite>Notice of intent to improve the energy efficiency of appliances and equipment through Amendment 15 to the</cite> Energy Efficiency Regulations</h4>
    <p >Notice is hereby given that the Department of Natural Resources is initiating the development of Amendment&nbsp;15 to the <cite>Energy Efficiency Regulations</cite> under the <cite>Energy Efficiency Act</cite> to increase the stringency of existing energy efficiency standards for 11&nbsp;product categories and to introduce such standards for the first time in Canada for 6&nbsp;product categories.</p>
    <h4><cite>Background</cite></h4>
    <p >The Government of Canada is committed to taking action&nbsp;on climate change by improving energy efficiency standards for appliances and equipment, and reducing regulatory burden through alignment with the United States and collaboration with provinces and territories. Improving energy efficiency standards provides both economic and environmental benefits to Canada.</p>
    <p >The <cite>Energy Efficiency Regulations</cite> were introduced in&nbsp;1995 as a means to reduce greenhouse gas emissions in Canada. They prescribe energy efficiency standards for energy-using products used in the residential, commercial and industrial sectors. They also prescribe labelling requirements for certain products to disclose and compare the energy use of a given product model relative to others in their category. The Regulations have been amended regularly to update existing energy efficiency standards and introduce standards for new products.</p>
    <p >In March&nbsp;2016, Canada and the United States committed to better align and further improve energy efficiency standards by&nbsp;2020. Moreover, Canada committed to align up to 10&nbsp;test methods and standards with the United States and Mexico by&nbsp;2019. This builds on the 2014&nbsp;Canada&ndash;United States Regulatory Cooperation Council commitment to align new and updated energy efficiency standards and test methods for energy-using equipment, to the extent practicable and permitted by law. Alignment of requirements can avoid creating unnecessary duplication and costs, reduce trade barriers and enhance consumer choice.</p>
    <p >Through the pan-Canadian framework and Energy and Mines Ministers' Conference, the federal, provincial and territorial governments have committed to working together to achieve climate change goals. Through this collaboration, governments agreed that the stringency of standards for some heating products could, for climatic and market reasons, exceed the stringency of those in the United States.</p>
    <h4><cite>Amendments to the Regulations</cite></h4>
    <p >Natural Resources Canada is proceeding with the development of Amendment&nbsp;15 to introduce or update energy efficiency standards and, where applicable, test methods and associated reporting and compliance requirements for the 17&nbsp;product categories listed below. The intent is to align&nbsp;11 of the 17&nbsp;product categories with energy efficiency standards in force or expected to be in force in the United States, subject to an analysis of Canadian market conditions. For the remaining 6 product categories, NRCan will undertake an analysis of the environmental and economic impacts of moving to more stringent standards.</p>
