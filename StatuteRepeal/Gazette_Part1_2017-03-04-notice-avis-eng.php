    <h3>CONSULTATIONS ON A POTENTIAL FREE TRADE AGREEMENT WITH CHINA</h3>
    <p >On September&nbsp;22, 2016, Prime Minister Justin Trudeau and Chinese Premier Li Keqiang announced that Canada and China had launched exploratory discussions for a possible Canada&ndash;China Free Trade Agreement (see press release). <a href="#footnote.56200" id="footnoteRef.56200">(see footnote 18)</a></p>
    <p >Parallel to the exploratory discussions, the Government of Canada is seeking the views of Canadians on how to best proceed regarding a possible Free Trade Agreement (FTA). This would include how to best improve market access and legal certainty and transparency for Canadian business, but also to understand any other related issues and concerns that are relevant when considering a possible FTA.</p>
    <p >The Government is soliciting views from businesses, civil society organizations, labour unions, academia, individual Canadians, Indigenous groups, and provincial and territorial governments on objectives, key interests and potential concerns, which will help outline the parameters of a potential initiative.</p>
    <h4>Background</h4>
    <p >China is Canada's second-largest single-country trading partner and a key market for Canada in the Asia-Pacific region. In&nbsp;2016, two-way merchandise trade amounted to $85.3&nbsp;billion. Canada's two-way foreign direct investment (FDI) relationship with China is also significant and reached $33.0&nbsp;billion at the end of&nbsp;2015. China is Canada's largest source of international students and third largest source of tourists.</p>
    <p >With a population of almost 1.4&nbsp;billion, a GDP of C$14.8&nbsp;trillion in&nbsp;2016, and a forecasted average annual economic growth rate of around 5.4% over the next 15&nbsp;years, China and the Chinese market present opportunities for Canadian companies in a wide variety of sectors. China's ongoing transition to a more service-oriented, consumer-based economy and its growing middle class have created demand for quality agricultural and agri-food products, advanced manufacturing, reliable sources of energy, financial and professional services, and innovative technologies and service solutions.</p>
    <p >A potential free trade agreement with China could result in economic gains for Canada by creating opportunities for Canadian firms, including small and medium-sized enterprises, that are looking to grow their business in international markets and by improving market access conditions for Canadian businesses operating in China. Canadian consumers may also benefit from a potential free trade agreement with China as a result of enhanced access to safe and affordable goods from China.</p>
    <p >Canadians may have concerns about China, including issues relating to the environment, labour, gender equality, rule of law and human rights. Canada's comprehensive dialogue with China is central to a healthy relationship and allows Canada to relay the concerns of Canadians and to engage with a country that hosts one sixth of the world's population. A free trade agreement with China would not deter Canada from urging and working with China to meet its international obligations in these areas. As Canada pursues future&nbsp;trade agreements, it will ensure policy flexibility is reserved to protect the health, safety and environment of Canadians.</p>
