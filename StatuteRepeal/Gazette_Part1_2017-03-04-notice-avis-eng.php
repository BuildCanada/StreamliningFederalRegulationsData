    <h3>CANADIAN ENVIRONMENTAL PROTECTION ACT,&nbsp;1999</h3>
    <h4><cite>Notice to interested parties&nbsp;&mdash; Publication of a discussion paper on a Clean Fuel Standard</cite></h4>
    <p >This notice is to provide interested stakeholders with the opportunity to comment on Environment and Climate Change Canada's discussion paper on the Clean Fuel Standard. The discussion paper can be obtained by visiting Environment and Climate Change Canada's CEPA Environmental Registry at http://www.ec.gc.ca/lcpe-cepa/default.asp?lang=En&amp;n=D7C913BB-1.</p>
    <p >Environment and Climate Change Canada is consulting with provinces, territories, stakeholders as well as Indigenous Peoples to develop a Clean Fuel Standard under the <cite>Canadian Environmental Protection Act</cite>,&nbsp;<cite>1999</cite> to reduce greenhouse gas (GHG) emissions from the use of fuels. The overall objective of the Clean Fuel Standard is to achieve 30&nbsp;megatonnes of annual reductions in GHG&nbsp;emissions by&nbsp;2030, contributing to Canada's effort to achieve its overall GHG mitigation target of a 30%&nbsp;emission reduction below 2005&nbsp;levels by&nbsp;2030.</p>
    <p >The Clean Fuel Standard will be a modern, flexible, performance-based approach that would incent the use of a broad range of lower carbon fuels, alternative energy sources and technologies, such as electricity, hydrogen, and renewable fuels, including renewable natural gas. It would address a broad suite of fuels, including gaseous, solid and liquid fuels, and would go beyond transportation fuels to include those used in industry, homes and buildings. The approach would not differentiate between crude oil types produced in Canada and those imported into Canada.</p>
    <p >The purpose of the discussion paper is to facilitate consultations by seeking early views to help inform the development of the regulatory framework in advance of developing a Clean Fuel Standard.</p>
    <p >Interested parties may submit comments by mail, email or fax, on or before April&nbsp;25, 2017, to the address below.</p>
