    <h2 id="nl4"  class="department">DEPARTMENT OF INNOVATION, SCIENCE AND ECONOMIC DEVELOPMENT</h2>
    <h3 >TELECOMMUNICATION ACT</h3>
    <h4><cite>Notice No. SMSE-006-16 &mdash; Release of CS-03, Parts I and VIII, Issue 9, Amendment 5</cite></h4>
    <p class="indent-1-0">Notice is hereby given that the Department of Innovation, Science and Economic Development has released the following documents:</p>
    <ul>
        <li>CS-03, Part I, Issue 9, Amendment 5, <cite>Requirements for Terminal Equipment (TE) and Related Access Arrangements Intended for Direct Connection to Analogue Wireline Facilities</cite>, which sets out the requirements for analogue terminal equipment intended for connection to the public switched network (e.g. equipment connected to loop start lines, tie trunks); and</li>
        <li>CS-03, Part VIII, Issue 9, Amendment 5, <cite>Requirements and Test Methods for Digital Subscriber Line (xDSL) Terminal Equipment</cite>, which sets out the technical requirements for a range of Digital Subscriber Line (xDSL) terminal equipment.</li>
    </ul>
    <h5 >General information</h5>
    <p class="indent-1-0">CS-03, Parts I and VIII, Issue&nbsp;9, Amendment 5, will both come into force upon publication on the Department's Web site (www.ic.gc.ca/spectrum).</p>
