    <h2 id="ne6"  class="department">DEPARTMENT OF TRANSPORT</h2>
    <h3 >CANADA SHIPPING ACT, 2001</h3>
    <h4><cite>Western Canada Marine Response Corporation</cite></h4>
    <p class="indent-1-1">Notice of an addition to the fees charged by Western Canada Marine Response Corporation pursuant to an arrangement required by subsection&nbsp;168(1) of the <cite>Canada Shipping Act, 2001 </cite></p>
    <h5 class="text-center"><cite>Description</cite></h5>
    <p class="indent-1-0">Western Canada Marine Response Corporation (&ldquo;WCMRC&rdquo;) is a certified response organization pursuant to section&nbsp;169 of the Act in respect of a rated capability of 10&nbsp;000&nbsp;tonnes and a geographic area covering the waters bordering British Columbia (including the shorelines associated with such waters) and excluding waters north of 60&deg;&nbsp;north latitude. This notice establishes an additional bulk oil cargo fee for the Trans Mountain Expansion Project, to be charged in addition to the registration and bulk oil cargo fees, and any amendments thereto, as published by notice in Part&nbsp;I of the <cite>Canada Gazette</cite>.</p>
    <h5 class="text-center"><cite>Definitions</cite></h5>
    <p class="indent-1-0">1. In this notice of fees,</p>
    <p>&ldquo;Act&rdquo; means the <cite>Canada Shipping Act, 2001</cite>. (<span xml:lang="fr" lang="fr"><cite>Loi</cite></span>)</p>
    <p>&ldquo;BOCF&rdquo; means the bulk oil cargo fee payable to WCMRC in relation to an arrangement required by subsections&nbsp;167(1) and&nbsp;168(1) of the Act, and any amendments thereto, as published, or as amended by notice in Part&nbsp;I of the <cite>Canada Gazette</cite>. [<span xml:lang="fr" lang="fr"><cite>droits sur les produits p&eacute;troliers en vrac (DPPV)</cite></span>]</p>
    <p>&ldquo;registration fees&rdquo; means the registration fees payable to WCMRC in relation to an arrangement required by subsections&nbsp;167(1) and&nbsp;168(1) of the Act, and any amendments thereto, as published by notice in Part&nbsp;I of the <cite>Canada Gazette</cite>. (<span xml:lang="fr" lang="fr"><cite>droits d&rsquo;enregistrement</cite></span>)</p>
    <p>&ldquo;TMEP BOCF&rdquo; means the Trans Mountain Expansion Project bulk oil cargo fee, a fee charged on crude oil shipments in bulk loaded onto a ship (bulk oil) and intended for international destinations and destinations north of 60&deg; north latitude, except shipments of jet fuel, from the Westridge oil handling facility owned by Trans Mountain Pipeline L.P. [<span xml:lang="fr" lang="fr"><cite>droits sur les produits p&eacute;troliers en vrac du projet d&rsquo;expansion de </cite></span><cite>Trans Mountain</cite><span xml:lang="fr" lang="fr"><cite> (DPPV PETM)</cite></span>]</p>
    <h5 class="text-center"><cite>Trans Mountain Expansion Project Bulk Oil Cargo Fee</cite></h5>
    <p class="indent-1-0">2. In relation to the arrangement with WCMRC for the Westridge oil handing facility, the TMEP BOCF will be determined by multiplying the total number of tonnes of oil loaded, within the meaning of the TMEP BOCF definition, by the TMEP BOCF rate per tonne of bulk oil.</p>
    <p class="indent-1-0">3. The TMEP BOCF rate applicable is two dollars and three and nine tenths cents ($2.039) per tonne of bulk oil, plus all applicable taxes, from March&nbsp;8, 2015.</p>
    <p class="indent-1-0">4. The TMEP BOCF will be charged in addition to the Registration Fees and BOCF, as amended from time to time, as published by notice in Part&nbsp;I of the <cite>Canada Gazette</cite>.</p>
    <p class="indent-1-0">January 30, 2015</p>
