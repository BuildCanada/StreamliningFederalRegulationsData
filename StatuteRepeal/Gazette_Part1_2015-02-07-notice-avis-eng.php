    <h2 id="ne7"  class="department">NOTICE OF VACANCY</h2>
    <h3 >BUSINESS DEVELOPMENT BANK OF CANADA</h3>
    <h4><cite>President (CEO) [full-time position]</cite></h4>
    <h4 >Salary range: From $306,300 to $360,300 Location: Montr&eacute;al, Quebec</h4>
    <p class="indent-1-0">The Business Development Bank of Canada (BDC) is a federal Crown corporation whose head office is located in Montr&eacute;al, Quebec. The BDC is dedicated to the creation, growth and success of Canadian enterprises, with a focus on small and medium-sized enterprises (SMEs). The BDC&rsquo;s purpose is to support Canadian entrepreneurship and, under the terms of its constitutive law, to do so in a financially sustainable manner. The BDC currently supports over 29&nbsp;000&nbsp;entrepreneurs through all stages of their development, providing services that include funding and consulting/advisory services. In providing these services, the BDC works with like-minded partners and financial institutions to enhance the support available to Canadian entrepreneurs, ensuring that its activities complement those of private sector financial institutions.</p>
    <p class="indent-1-0">This exceptional organization is now seeking its next president. The President is accountable for providing strategic business leadership and advancing the BDC&rsquo;s public policy mandate to promote and support entrepreneurship in Canada. The President is a business visionary who leads the BDC to play its complementary role in the financial services sector while meeting Treasury Board requirements on long-term sustainability.</p>
    <p class="indent-1-0">The President&rsquo;s leadership fosters integrity and ethical business practices and sets the tone at the top for the management of the BDC&rsquo;s resources and assets. The President oversees the effectiveness and efficiency of the BDC&rsquo;s business operations and ensures that appropriate governance and management structures, policies and procedures are in place to achieve its mandated objectives.</p>
    <p class="indent-1-0">The ideal candidate would possess a university degree (preferably at the Master&rsquo;s level) from a recognized institution in a business discipline (e.g.&nbsp;business, finance, economics) or a comparable background of education, business training and experience. A professional accounting designation (CPA) would be considered an asset.</p>
    <p class="indent-1-0">The ideal candidate would possess proven transactional leadership experience in deal evaluation and in leading transactions in a range of financial or commercial settings, ideally having worked with small and medium-sized enterprises (SMEs); significant senior executive leadership experience in large complex organizations, ideally with some international business focus, including the management of human and financial resources; experience reporting to, and/or serving on, a board of directors and in maintaining productive relationships with senior-level stakeholders; experience in strategic management, business development and risk management associated with the BDC&rsquo;s major lines of business and in those management techniques that promote good governance and improve organizational performance. Experience as an entrepreneur of a small or medium-sized business would be considered an asset.</p>
    <p class="indent-1-0">The ideal candidate would possess knowledge and understanding of the Business Development Bank of Canada&rsquo;s mandate, business activities and public policy concerns, as well as its governing legislation, including the <cite>Business Development Bank of Canada Act</cite> and the <cite>Financial Administration Act</cite>; sound knowledge and understanding of the current economic context and challenges faced by SMEs and their key leaders and entrepreneurs, including emerging trends, competitive forces, mentoring and coaching for entrepreneurs as well as the commercial financial services offered to business enterprises; knowledge and understanding of the complementary role of the BDC within the financial service sector and its role and responsibilities within the framework of the Government of Canada&rsquo;s economic agenda as an agent of policy for the Government; knowledge of corporate lending practices, investments and transaction structures and the financial disciplines associated with the development and management of financing instruments; and knowledge of sound corporate governance and management principles and practices and the financial management requirements, particularly risk management, of a large financial services organization.</p>
    <p class="indent-1-0">The ideal candidate would have the ability to apply strategic thinking, innovation and change management skills when leading the development of business strategies, corporate planning and policy-making activities; the demonstrated ability to anticipate, assess and address business risks, particularly as they relate to complex financial transactions, and to exercise sound judgment and significant decision-making skills in analyzing and resolving complex contract and business matters; proven leadership in building a strong organizational culture, and in attracting, mentoring and coaching talented staff; the ability to focus the energies of the BDC&rsquo;s employees to motivate them to achieve corporate objectives; the ability to develop effective working relationships with the Board of Directors, the Minister and his or her office, the Deputy Minister and the BDC&rsquo;s partners and stakeholders; and the ability to act as the spokesperson, with superior communication skills, both written and oral, in representing the BDC with stakeholders, media, public institutions, governments and other organizations.</p>
    <p class="indent-1-0">The ideal candidate would possess high ethical standards, be a strategic and innovative &ldquo;thought leader,&rdquo; known for integrity and impartiality, with excellent interpersonal skills and would possess sound judgement.</p>
    <p class="indent-1-0">Proficiency in both official languages would be preferred.</p>
    <p class="indent-1-0">The successful candidate must reside in or be willing to relocate to Montr&eacute;al, Quebec, or to a location within reasonable commuting distance. The successful candidate must be willing to travel across Canada and internationally as required.</p>
    <p class="indent-1-0">A person may not be appointed as President if the individual is neither a Canadian citizen nor a permanent resident within the meaning of subsection&nbsp;2(1) of the <cite>Immigration and Refugee Protection Act</cite>; a Canadian citizen who is not ordinarily resident in Canada; a permanent resident, within the meaning of subsection&nbsp;2(1) of the <cite>Immigration and Refugee Protection Act</cite>, who has been ordinarily resident in Canada for more than one year after first becoming eligible to apply for Canadian citizenship; a member of the Senate or House of Commons or a member of the legislature of a province; or employed in the public service of a province.</p>
    <p class="indent-1-0">The Government is committed to ensuring that its appointments are representative of Canada&rsquo;s regions and official languages, as well as of women, Aboriginal peoples, disabled persons and visible minorities.</p>
    <p class="indent-1-0">The selected candidate must comply with the <cite>Ethical and Political Activity Guidelines for Public Office Holders</cite>. The Guidelines are available on the Governor in Council Appointments Web site, under &ldquo;Reference Material,&rdquo; at <a href="http://www.appointments-nominations.gc.ca/index.asp?lang=eng">www.appointments-nominations.gc.ca/index.asp?lang=eng</a>.</p>
    <p class="indent-1-0">The selected candidate will be subject to the <cite>Conflict of Interest Act</cite>. Public office holders appointed on a full-time basis must submit to the Office of the Conflict of Interest and Ethics Commissioner, within 60&nbsp;days of appointment, a confidential report in which they disclose all of their assets, liabilities and outside activities. For more information, please visit the Office of the Conflict of Interest and Ethics Commissioner&rsquo;s Web site at <a href="http://ciec-ccie.gc.ca/Default.aspx?pid=1&amp;lang=en">http://ciec-ccie.gc.ca/Default.aspx?pid=1&amp;lang=en</a>.</p>
    <p class="indent-1-0">This notice has been placed in the <cite>Canada Gazette</cite> to assist the Governor in Council in identifying qualified candidates for this position. It is not, however, intended to be the sole means of recruitment.</p>
    <p class="indent-1-0">Further details about the organization and its activities can be found on its Web site at <a href="http://www.bdc.ca">http://www.bdc.ca</a>.</p>
    <p class="indent-1-0">Should you be interested in learning more about this leadership opportunity, please visit <a href="http://www.odgersberndtson.ca">www.odgersberndtson.ca</a> for the full job specification. To apply in confidence, please forward your curriculum vitae and a letter of introduction to nathalie.francisci@odgersberndtson.ca or paul.stanley@odgersberndtson.ca. All submissions will be acknowledged.</p>
    <p class="indent-1-0">English and French notices of vacancies will be produced in an alternative format upon request. For further information, please contact GICA-NGEC@pco-bcp.gc.ca.</p>
